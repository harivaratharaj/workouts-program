package OOPS;

public class Encapsulation {
    private  int num;
    private  double digit;
    private  String  name;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public double getDigit() {
		return digit;
	}
	public void setDigit(double digit) {
		this.digit = digit;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
    
}
